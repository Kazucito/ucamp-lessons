/*Variables y tipos de datos primitivos*/
/*3 Palabras reservadas : let, var y const*/

//let: es de bloque, cambia su valor en el tiempo 
let nombre = "Yo" //string o cadena de texto

//var: es una variable global y cambia su valor en el tiempo
var edad = 25 //number o numerico 

//const: es una variable de bloque y no cambia su valor en el tiempo
const esMujer = false //boolean o booleano
const esHombre = true 

//null
let nulo = null //no existe valor alguno

//undefined
let undefined = undefined //puede tener un valor mas adelante

//Bloque: es un pedazo de codigo encapsulado en la operacion 
